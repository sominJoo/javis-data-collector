from __future__ import annotations

import re


def slugify(s: str) -> str:
    """report_type_name → SKILL name 슬러그."""
    t = re.sub(r"[^\w가-힣\s-]", "", s or "").strip().lower()
    t = re.sub(r"\s+", "-", t)
    return t[:64] or "report-type"


